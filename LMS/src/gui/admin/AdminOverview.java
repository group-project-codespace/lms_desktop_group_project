    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:

        if (evt.getClickCount() == 1) {
            int row = jTable1.getSelectedRow();

            String fname = String.valueOf(jTable1.getValueAt(row, 2));
            jTextField1.setText(fname);
            jTextField1.setEditable(false);

            String lname = String.valueOf(jTable1.getValueAt(row, 3));
            jTextField5.setText(lname);
            jTextField5.setEditable(false);

            String userName = String.valueOf(jTable1.getValueAt(row, 7));
            jTextField2.setText(userName);
            jTextField2.setEditable(false);

            String Password = String.valueOf(jTable1.getValueAt(row, 8));
            jPasswordField1.setText(Password);
            jPasswordField1.setEditable(false);

            String Address = String.valueOf(jTable1.getValueAt(row, 9));
            jTextField10.setText(Address);
            jTextField10.setEditable(false);

            String NIC = String.valueOf(jTable1.getValueAt(row, 0));
            jTextField4.setText(NIC);
            jTextField4.setEditable(false);

            String Email = String.valueOf(jTable1.getValueAt(row, 4));
            jTextField6.setText(Email);
            jTextField6.setEditable(false);

            String Mobile = String.valueOf(jTable1.getValueAt(row, 1));
            jTextField7.setText(Mobile);
            jTextField7.setEditable(false);

            String Position = String.valueOf(jTable1.getValueAt(row, 5));
            jTextField9.setText(Position);
            jTextField9.setEditable(false);

            String Gender = String.valueOf(jTable1.getValueAt(row, 6));
            jTextField8.setText(Gender);
            jTextField8.setEditable(false);

        }
    }//GEN-LAST:event_jTable1MouseClicked

