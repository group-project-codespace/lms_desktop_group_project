    private void loadAdmin() {
        try {
            ResultSet resultSet = connection.execute("SELECT * FROM `admin` INNER JOIN `position` ON `admin`.`position_id`=`position`.`id`"
                    + "INNER JOIN `address` ON `admin` .`address_id` =`address`.`id` INNER JOIN `gender` ON `admin`.`gender_id`=`gender`.`id`");

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (resultSet.next()) {
                Vector<String> vector = new Vector<>();
                vector.add(resultSet.getString("nic"));
                vector.add(resultSet.getString("mobile_number"));
                vector.add(resultSet.getString("first_name"));
                vector.add(resultSet.getString("last_name"));
                vector.add(resultSet.getString("email"));
                vector.add(resultSet.getString("position.name"));
                vector.add(resultSet.getString("gender.name"));
                vector.add(resultSet.getString("username"));
                vector.add(resultSet.getString("password"));
                vector.add(resultSet.getString("address.line1") + resultSet.getString("address.line2") );
//                vector.add(resultSet.getString("address.line2"));

//                System.out.println(vector);
                model.addRow(vector);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

