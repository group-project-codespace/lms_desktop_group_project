    private void loadData() {
        try {
            DefaultTableModel defaultTableModel = (DefaultTableModel) jTable1.getModel();
            defaultTableModel.setRowCount(0);

            ResultSet resultSet = connection.execute("SELECT * FROM `instructor` INNER JOIN `subject` ON `instructor`.`subject_id`=`subject`.`id` INNER JOIN `gender` ON `instructor`.`gender_id`= `gender`.`id` INNER JOIN `address` ON `instructor`.`address_id`=`address`.`id`");

            while (resultSet.next()) {
                Vector vector = new Vector();
                vector.add(resultSet.getString("nic"));
                vector.add(resultSet.getString("mobile_number"));
                vector.add(resultSet.getString("first_name"));
                vector.add(resultSet.getString("last_name"));
                vector.add(resultSet.getString("email"));
                vector.add(resultSet.getString("subject.name"));
                vector.add(resultSet.getString("gender.name"));
                vector.add(resultSet.getString("address.line1") + resultSet.getString("address.line2"));

                defaultTableModel.addRow(vector);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

